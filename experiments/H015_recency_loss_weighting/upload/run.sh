#!/bin/bash
# Platform run.sh for H015_recency_loss_weighting.
# Recency-aware loss weighting (cohort drift mitigation, L2 검증).
# - Per-batch label_time min-max → percentile → linear weight in [0.5, 1.5].
# - Mean weight = 1.0 (loss scale 보존).
# - H010 mechanism (NS xattn) + H008 mechanism (DCN-V2 fusion) + envelope byte-identical.
# - 코드 변경: dataset.py (label_time batch dict 노출), trainer.py (loss weighting branch), train.py (CLI flags).
# - model.py / infer.py / utils.py / make_schema.py / local_validate.py / ns_groups.json / requirements.txt: byte-identical.
# - Goal: 4-layer ceiling diagnosis 의 마지막 가설 L2 (cohort drift hard ceiling) 직접 검증.
#   9 H 의 OOF AUC stable (0.857~0.860) / Platform 변동 패턴 = train/eval distribution shift.
#   Recency weighting → train 이 platform 분포에 closer.
# - §17.4 temporal_cohort 신규 카테고리 first-touch (rotation FREE).
# - §18 inference 인프라 룰 inherit (infer.py 변경 없음).
# Generated by H015 build flow — do not hand-edit on platform.
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
export PYTHONPATH="${SCRIPT_DIR}:${PYTHONPATH:-}"

# ---------------------------------------------------------------------------
# Platform must set TRAIN_DATA_PATH and TRAIN_CKPT_PATH. Other paths derive.
# ---------------------------------------------------------------------------
: "${TRAIN_DATA_PATH:?TRAIN_DATA_PATH not set by platform}"
: "${TRAIN_CKPT_PATH:?TRAIN_CKPT_PATH not set by platform}"
: "${TRAIN_LOG_PATH:=${TRAIN_CKPT_PATH}/logs}"
: "${TRAIN_TF_EVENTS_PATH:=${TRAIN_CKPT_PATH}/tf_events}"
: "${TRAIN_WORK_PATH:=${TRAIN_CKPT_PATH}/work}"
mkdir -p "${TRAIN_CKPT_PATH}" "${TRAIN_LOG_PATH}" "${TRAIN_TF_EVENTS_PATH}" "${TRAIN_WORK_PATH}"
export TRAIN_LOG_PATH TRAIN_TF_EVENTS_PATH

echo "[run.sh] DATA=${TRAIN_DATA_PATH}"
echo "[run.sh] CKPT=${TRAIN_CKPT_PATH}"
echo "[run.sh] mode=recency-loss-weighting (H010 mechanism + per-batch linear recency weight [0.5, 1.5])"

# ---------------------------------------------------------------------------
# Train (H010 mechanism + envelope, recency-aware loss weighting added).
# Diff vs H010/upload/run.sh:
#   --use_recency_loss_weighting    H015 NEW: per-batch linear recency weighting
#   --recency_weight_min 0.5        H015 NEW: oldest sample weight (default)
#   --recency_weight_max 1.5        H015 NEW: newest sample weight (default, mean = 1.0)
#   --batch_size 2048               H015 NEW (사용자 prior H regime 명시 bake)
#   --lr 1e-4                       H015 NEW (H010 default 명시 bake)
# H010 mechanism flags (--use_ns_to_s_xattn --ns_xattn_num_heads 4 --log_attn_entropy) 그대로.
# H008 mechanism flags (--fusion_type dcn_v2 --dcn_v2_num_layers 2 --dcn_v2_rank 8) 그대로.
# seq_max_lens H010 default (64-128) — long-seq REFUTED, mechanism axis 다름.
# ---------------------------------------------------------------------------
python3 -u "${SCRIPT_DIR}/train.py" \
    --num_epochs 10 \
    --patience 3 \
    --seed 42 \
    --batch_size 2048 \
    --lr 1e-4 \
    --ns_tokenizer_type rankmixer \
    --user_ns_tokens 5 \
    --item_ns_tokens 2 \
    --num_queries 2 \
    --ns_groups_json "" \
    --emb_skip_threshold 1000000 \
    --num_workers 2 \
    --buffer_batches 4 \
    --train_ratio 0.3 \
    --seq_max_lens "seq_a:64,seq_b:64,seq_c:128,seq_d:128" \
    --use_label_time_split \
    --oof_user_ratio 0.1 \
    --split_seed 42 \
    --fusion_type dcn_v2 \
    --dcn_v2_num_layers 2 \
    --dcn_v2_rank 8 \
    --use_ns_to_s_xattn \
    --ns_xattn_num_heads 4 \
    --log_attn_entropy \
    --use_recency_loss_weighting \
    --recency_weight_min 0.5 \
    --recency_weight_max 1.5 \
    "$@"

echo "[run.sh] training complete (recency-loss-weighting); metrics at ${TRAIN_CKPT_PATH}/metrics.json"
