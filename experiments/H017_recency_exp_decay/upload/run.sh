#!/bin/bash
# Platform run.sh for H017_recency_exp_decay.
# H015 sub-form variant — recency_weight_form 'linear' → 'exp' (auto-normalized to mean=1.0).
# - H015 envelope + mechanism + 모든 설정 byte-identical except --recency_weight_form exp.
# - exp form: w_raw = w_min × (w_max/w_min)^pct, then normalize to mean=1.0.
# - Same range [0.5, 1.5] → after normalize ≈ [0.55, 1.65] (slight asymmetry, recent emphasis).
# - Goal: linear recency 가 noise 일 때 exp curve 가 더 강한 효과 검증.
# - L2 (cohort drift) sub-form — H015 와 paired 비교 가능 (mechanism shape only).
# - §17.2 single mutation (form change only, hyperparameter range 동일).
# Generated by H017 build flow — do not hand-edit on platform.
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
export PYTHONPATH="${SCRIPT_DIR}:${PYTHONPATH:-}"

: "${TRAIN_DATA_PATH:?TRAIN_DATA_PATH not set by platform}"
: "${TRAIN_CKPT_PATH:?TRAIN_CKPT_PATH not set by platform}"
: "${TRAIN_LOG_PATH:=${TRAIN_CKPT_PATH}/logs}"
: "${TRAIN_TF_EVENTS_PATH:=${TRAIN_CKPT_PATH}/tf_events}"
: "${TRAIN_WORK_PATH:=${TRAIN_CKPT_PATH}/work}"
mkdir -p "${TRAIN_CKPT_PATH}" "${TRAIN_LOG_PATH}" "${TRAIN_TF_EVENTS_PATH}" "${TRAIN_WORK_PATH}"
export TRAIN_LOG_PATH TRAIN_TF_EVENTS_PATH

echo "[run.sh] DATA=${TRAIN_DATA_PATH}"
echo "[run.sh] CKPT=${TRAIN_CKPT_PATH}"
echo "[run.sh] mode=recency-exp-decay (H015 base + recency_weight_form exp)"

# ---------------------------------------------------------------------------
# Diff vs H015/upload/run.sh:
#   --recency_weight_form exp        H017 NEW (was 'linear' default in H015)
# All other flags H015 byte-identical.
# ---------------------------------------------------------------------------
python3 -u "${SCRIPT_DIR}/train.py" \
    --num_epochs 10 \
    --patience 3 \
    --seed 42 \
    --batch_size 2048 \
    --lr 1e-4 \
    --ns_tokenizer_type rankmixer \
    --user_ns_tokens 5 \
    --item_ns_tokens 2 \
    --num_queries 2 \
    --ns_groups_json "" \
    --emb_skip_threshold 1000000 \
    --num_workers 2 \
    --buffer_batches 4 \
    --train_ratio 0.3 \
    --seq_max_lens "seq_a:64,seq_b:64,seq_c:128,seq_d:128" \
    --use_label_time_split \
    --oof_user_ratio 0.1 \
    --split_seed 42 \
    --fusion_type dcn_v2 \
    --dcn_v2_num_layers 2 \
    --dcn_v2_rank 8 \
    --use_ns_to_s_xattn \
    --ns_xattn_num_heads 4 \
    --log_attn_entropy \
    --use_recency_loss_weighting \
    --recency_weight_min 0.5 \
    --recency_weight_max 1.5 \
    --recency_weight_form exp \
    "$@"

echo "[run.sh] training complete (recency-exp-decay); metrics at ${TRAIN_CKPT_PATH}/metrics.json"
