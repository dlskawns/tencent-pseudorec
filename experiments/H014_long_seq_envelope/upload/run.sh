#!/bin/bash
# Platform run.sh for H014_long_seq_envelope.
# Long-seq envelope expansion (L4 ceiling diagnosis).
# - Single mutation: seq_max_lens "64-64-128-128" → "192-192-192-192" (uniform 192: a/b 3×, c/d 1.5×).
# - OOM mitigation iter-2: 원래 4× per domain (256-512) → uniform 256 → uniform 192. V100 19 GB partition + multi-process MPS 환경에서 batch 2048 + seq 256 도 22 GB allocated OOM. seq 192 = attention 56% of 256, 약 9.5 GiB → total ~15-16 GiB 안전.
# - H010 mechanism (NS→S xattn) + H008 mechanism (DCN-V2 fusion) byte-identical.
# - 코드 변경 0 (model.py / train.py / infer.py / trainer.py / dataset.py byte-identical with H010).
# - Data motivation: §3.5 domain p90 1393~2215 vs 현재 truncate 64-128 → 95%+ 정보 손실.
# - L4 가 4-layer ceiling diagnosis 의 마지막 unexplored axis (L1 H013 REFUTED, L3 H011/H012/H013 retire).
# - §17.2 envelope mutation = "structural" 인정 (input data shape 변경, mechanism 0).
# Generated by H014 build flow — do not hand-edit on platform.
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
export PYTHONPATH="${SCRIPT_DIR}:${PYTHONPATH:-}"
# H014 OOM mitigation: PyTorch CUDA allocator 단편화 완화 (multi-process MPS 환경)
export PYTORCH_CUDA_ALLOC_CONF=expandable_segments:True

# ---------------------------------------------------------------------------
# Platform must set TRAIN_DATA_PATH and TRAIN_CKPT_PATH. Other paths derive.
# ---------------------------------------------------------------------------
: "${TRAIN_DATA_PATH:?TRAIN_DATA_PATH not set by platform}"
: "${TRAIN_CKPT_PATH:?TRAIN_CKPT_PATH not set by platform}"
: "${TRAIN_LOG_PATH:=${TRAIN_CKPT_PATH}/logs}"
: "${TRAIN_TF_EVENTS_PATH:=${TRAIN_CKPT_PATH}/tf_events}"
: "${TRAIN_WORK_PATH:=${TRAIN_CKPT_PATH}/work}"
mkdir -p "${TRAIN_CKPT_PATH}" "${TRAIN_LOG_PATH}" "${TRAIN_TF_EVENTS_PATH}" "${TRAIN_WORK_PATH}"
export TRAIN_LOG_PATH TRAIN_TF_EVENTS_PATH

echo "[run.sh] DATA=${TRAIN_DATA_PATH}"
echo "[run.sh] CKPT=${TRAIN_CKPT_PATH}"
echo "[run.sh] mode=long-seq-envelope (H010 mechanism + seq_max_lens uniform 192: a/b 3×, c/d 1.5×)"

# ---------------------------------------------------------------------------
# Train (H010 mechanism + envelope, only seq_max_lens changed).
# Diff vs H010/upload/run.sh:
#   --seq_max_lens "seq_a:192,seq_b:192,seq_c:192,seq_d:192"   H014 NEW
#       (a/b: 64→192 = 3× expansion, c/d: 128→192 = 1.5× expansion).
#       OOM mitigation iter-2 (V100 19 GB partition + multi-process MPS):
#         orig plan: 4× per domain (256/256/512/512) → attention ~43 GiB OOM.
#         iter-1: uniform 256 → 22 GiB allocated 도 부족 (FFN+optim 누락 추정).
#         iter-2 (final): uniform 192 → attention 56% of 256 → ~9.5 GiB,
#         total ~15-16 GiB ≤ 19 GiB partition 안전.
#   --batch_size 1024                                          H014 NEW iter-3 OOM mitigation (사용자 prior H 들 regime 2048 절반 — batch 2048 + seq 192 도 25 GiB allocated 부족. batch 1024 → ~12.5 GiB 안전). paired Δ confound 인정.
#   --lr 1e-4                                                  H014 NEW (H010 default 명시 bake, H013 8e-4 REFUTED 복귀)
#   PYTORCH_CUDA_ALLOC_CONF=expandable_segments:True           H014 NEW (allocator 단편화 완화)
# All other flags H010 byte-identical.
# ---------------------------------------------------------------------------
python3 -u "${SCRIPT_DIR}/train.py" \
    --num_epochs 10 \
    --patience 3 \
    --seed 42 \
    --batch_size 1024 \
    --lr 1e-4 \
    --ns_tokenizer_type rankmixer \
    --user_ns_tokens 5 \
    --item_ns_tokens 2 \
    --num_queries 2 \
    --ns_groups_json "" \
    --emb_skip_threshold 1000000 \
    --num_workers 2 \
    --buffer_batches 4 \
    --train_ratio 0.3 \
    --seq_max_lens "seq_a:192,seq_b:192,seq_c:192,seq_d:192" \
    --use_label_time_split \
    --oof_user_ratio 0.1 \
    --split_seed 42 \
    --fusion_type dcn_v2 \
    --dcn_v2_num_layers 2 \
    --dcn_v2_rank 8 \
    --use_ns_to_s_xattn \
    --ns_xattn_num_heads 4 \
    --log_attn_entropy \
    "$@"

echo "[run.sh] training complete (long-seq-envelope); metrics at ${TRAIN_CKPT_PATH}/metrics.json"
