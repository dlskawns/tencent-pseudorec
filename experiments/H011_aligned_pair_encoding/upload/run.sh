#!/bin/bash
# Platform run.sh for H011_aligned_pair_encoding.
# Aligned <id, weight> pair encoding at input embedding lookup (Option α).
# - For verified shared fids {62, 63, 64, 65, 66, 89, 90, 91}: per-row L1-normalized
#   weighted mean using user_dense_feats slice as weights (parameter-free).
# - non-aligned fids + dense-only {61, 87}: baseline mean-pool / user_dense_proj 그대로.
# - H010 NS→S xattn + H008 DCN-V2 anchor 모두 byte-identical 유지 (stacking on champion).
# - input-stage mutation, NS xattn / DCN-V2 fusion 입력 텐서 byte-identical → H009 위치 충돌 회피.
# - parameter-free (params 추가 0); P0 audit + gap #3 verified.
# - §17.4 feature_engineering 신규 first-touch (rotation FREE).
# - §18 inference 인프라 룰 inherit.
# Generated by H011 build flow — do not hand-edit on platform.
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
export PYTHONPATH="${SCRIPT_DIR}:${PYTHONPATH:-}"

# ---------------------------------------------------------------------------
# Platform must set TRAIN_DATA_PATH and TRAIN_CKPT_PATH. Other paths derive.
# ---------------------------------------------------------------------------
: "${TRAIN_DATA_PATH:?TRAIN_DATA_PATH not set by platform}"
: "${TRAIN_CKPT_PATH:?TRAIN_CKPT_PATH not set by platform}"
: "${TRAIN_LOG_PATH:=${TRAIN_CKPT_PATH}/logs}"
: "${TRAIN_TF_EVENTS_PATH:=${TRAIN_CKPT_PATH}/tf_events}"
: "${TRAIN_WORK_PATH:=${TRAIN_CKPT_PATH}/work}"
mkdir -p "${TRAIN_CKPT_PATH}" "${TRAIN_LOG_PATH}" "${TRAIN_TF_EVENTS_PATH}" "${TRAIN_WORK_PATH}"
export TRAIN_LOG_PATH TRAIN_TF_EVENTS_PATH

echo "[run.sh] DATA=${TRAIN_DATA_PATH}"
echo "[run.sh] CKPT=${TRAIN_CKPT_PATH}"
echo "[run.sh] mode=aligned-pair-encoding (H010 anchor + H011 weighted-mean lookup)"

# ---------------------------------------------------------------------------
# Train (H010 envelope + H010 mechanism + H008 anchor + H011 NEW).
# Diff vs H010/upload/run.sh:
#   --use_aligned_pair_encoding     H011 NEW: aligned input-stage weighted multiply
#   --aligned_pair_fids ...         H011 NEW: 8 verified shared fids (default)
# H010 mechanism flags (--use_ns_to_s_xattn --ns_xattn_num_heads 4 --log_attn_entropy) 그대로.
# H008 mechanism flags (--fusion_type dcn_v2 --dcn_v2_num_layers 2 --dcn_v2_rank 8) 그대로.
# ---------------------------------------------------------------------------
python3 -u "${SCRIPT_DIR}/train.py" \
    --num_epochs 10 \
    --patience 3 \
    --seed 42 \
    --ns_tokenizer_type rankmixer \
    --user_ns_tokens 5 \
    --item_ns_tokens 2 \
    --num_queries 2 \
    --ns_groups_json "" \
    --emb_skip_threshold 1000000 \
    --num_workers 2 \
    --buffer_batches 4 \
    --train_ratio 0.3 \
    --seq_max_lens "seq_a:64,seq_b:64,seq_c:128,seq_d:128" \
    --use_label_time_split \
    --oof_user_ratio 0.1 \
    --split_seed 42 \
    --fusion_type dcn_v2 \
    --dcn_v2_num_layers 2 \
    --dcn_v2_rank 8 \
    --use_ns_to_s_xattn \
    --ns_xattn_num_heads 4 \
    --log_attn_entropy \
    --use_aligned_pair_encoding \
    --aligned_pair_fids 62 63 64 65 66 89 90 91 \
    "$@"

echo "[run.sh] training complete (aligned-pair-encoding); metrics at ${TRAIN_CKPT_PATH}/metrics.json"
