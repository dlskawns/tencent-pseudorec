#!/bin/bash
# Platform run.sh for H018_per_user_recency_weighting.
# Per-user exp time-decay loss weighting (replaces H015 per-batch linear).
# Mutation per §17.2: granularity change (per-batch label_time → per-user
# days_since_last_event). H010 mechanism (NS xattn) + H008 mechanism
# (DCN-V2 fusion) + envelope byte-identical.
# - dataset.py: per-domain max(seq_ts) accumulation → days_since_last_event
#   exposed in batch dict (~10 lines).
# - trainer.py: per-user exp decay weighting branch (~15 lines, replaces H015
#   per-batch linear branch).
# - train.py: H018 argparse (4 args, replaces H015 3 args) + Trainer keys
#   + §18.8 emit_train_summary at end (~17 lines).
# - model.py / infer.py / utils.py / make_schema.py / local_validate.py /
#   ns_groups.json / requirements.txt: byte-identical (H015 fork = H010 fork).
# - Goal: H015 marginal +0.0002pt 의 per-batch coarse 약점이 진짜 issue 인지
#   per-user fine granularity 로 검증. cohort drift hard ceiling 가설의
#   recency mechanism class 안 finer attempt.
# - control_exp_id = H015 (paired Δ → granularity isolation).
# - §17.2 single mutation (granularity), §17.4 temporal_cohort 4-entry
#   (challengers.md 재진입 정당화).
# - §18.7 carry-forward (label_time fill_null in dataset.py via H015).
# - §18.8 emit_train_summary (FIRST H to use, verify-claim parser anchor).
# Generated by H018 build flow — do not hand-edit on platform.
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
export PYTHONPATH="${SCRIPT_DIR}:${PYTHONPATH:-}"
export EXP_ID="H018_per_user_recency_weighting"   # §18.8 SUMMARY identity

# ---------------------------------------------------------------------------
# Platform must set TRAIN_DATA_PATH and TRAIN_CKPT_PATH. Other paths derive.
# ---------------------------------------------------------------------------
: "${TRAIN_DATA_PATH:?TRAIN_DATA_PATH not set by platform}"
: "${TRAIN_CKPT_PATH:?TRAIN_CKPT_PATH not set by platform}"
: "${TRAIN_LOG_PATH:=${TRAIN_CKPT_PATH}/logs}"
: "${TRAIN_TF_EVENTS_PATH:=${TRAIN_CKPT_PATH}/tf_events}"
: "${TRAIN_WORK_PATH:=${TRAIN_CKPT_PATH}/work}"
mkdir -p "${TRAIN_CKPT_PATH}" "${TRAIN_LOG_PATH}" "${TRAIN_TF_EVENTS_PATH}" "${TRAIN_WORK_PATH}"
export TRAIN_LOG_PATH TRAIN_TF_EVENTS_PATH

echo "[run.sh] DATA=${TRAIN_DATA_PATH}"
echo "[run.sh] CKPT=${TRAIN_CKPT_PATH}"
echo "[run.sh] EXP_ID=${EXP_ID}"
echo "[run.sh] mode=H018 per-user exp recency weighting (tau=14d, clip [0.1, 3.0])"

# ---------------------------------------------------------------------------
# Train (H010 mechanism + envelope, per-user recency weighting added).
# Diff vs H015/upload/run.sh:
#   −−use_recency_loss_weighting       H015 OFF (legacy, replaced by H018)
#   −−recency_weight_min/max           H015 OFF
#   ++ −−use_per_user_recency           H018 NEW: per-user exp decay
#   ++ −−recency_tau_days 14.0          H018 NEW: decay constant
#   ++ −−recency_weight_clip_min 0.1    H018 NEW: variance safety lower
#   ++ −−recency_weight_clip_max 3.0    H018 NEW: variance safety upper
# All H010/H008 mechanism flags 그대로 유지.
# ---------------------------------------------------------------------------
python3 -u "${SCRIPT_DIR}/train.py" \
    --num_epochs 10 \
    --patience 3 \
    --seed 42 \
    --batch_size 2048 \
    --lr 1e-4 \
    --ns_tokenizer_type rankmixer \
    --user_ns_tokens 5 \
    --item_ns_tokens 2 \
    --num_queries 2 \
    --ns_groups_json "" \
    --emb_skip_threshold 1000000 \
    --num_workers 2 \
    --buffer_batches 4 \
    --train_ratio 0.3 \
    --seq_max_lens "seq_a:64,seq_b:64,seq_c:128,seq_d:128" \
    --use_label_time_split \
    --oof_user_ratio 0.1 \
    --split_seed 42 \
    --fusion_type dcn_v2 \
    --dcn_v2_num_layers 2 \
    --dcn_v2_rank 8 \
    --use_ns_to_s_xattn \
    --ns_xattn_num_heads 4 \
    --log_attn_entropy \
    --use_per_user_recency \
    --recency_tau_days 14.0 \
    --recency_weight_clip_min 0.1 \
    --recency_weight_clip_max 3.0 \
    "$@"

echo "[run.sh] training complete (H018 per-user recency); metrics at ${TRAIN_CKPT_PATH}/metrics.json"
echo "[run.sh] §18.8 SUMMARY block above (copy block bracketed by '==== TRAIN SUMMARY' / '==== END SUMMARY')"
