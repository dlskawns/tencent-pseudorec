#!/bin/bash
# Platform run.sh for H012_multi_domain_fusion.
# Multi-domain MoE on NS-tokens (MMoE single-layer).
# - 4 expert FFNs (= 도메인 a/b/c/d 매핑) + softmax gate per NS-token.
# - 통합 위치: H010 NS xattn 출력 직후, query decoder + DCN-V2 fusion 전.
#   NS dimension preserved → DCN-V2 fusion input token stack unchanged.
# - parameter-free anchor + ~33K params 추가 (4 × FFN 64→128→64 + gate).
# - §10.9 gate entropy 룰: collapse threshold 0.5×log(4)≈0.69, uniform log(4)≈1.39.
# - Source: MMoE (Ma et al. KDD 2018, arXiv:1810.10739) — task→domain mapping.
# - 데이터 motivation: 4 도메인 Jaccard ≤ 0.10 (eda/out/domain_facts.json).
# - §17.4 multi_domain_fusion 신규 카테고리 first-touch (rotation FREE).
# - §18 inference 인프라 룰 inherit (H010 패키지).
# Generated by H012 build flow — do not hand-edit on platform.
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
export PYTHONPATH="${SCRIPT_DIR}:${PYTHONPATH:-}"

# ---------------------------------------------------------------------------
# Platform must set TRAIN_DATA_PATH and TRAIN_CKPT_PATH. Other paths derive.
# ---------------------------------------------------------------------------
: "${TRAIN_DATA_PATH:?TRAIN_DATA_PATH not set by platform}"
: "${TRAIN_CKPT_PATH:?TRAIN_CKPT_PATH not set by platform}"
: "${TRAIN_LOG_PATH:=${TRAIN_CKPT_PATH}/logs}"
: "${TRAIN_TF_EVENTS_PATH:=${TRAIN_CKPT_PATH}/tf_events}"
: "${TRAIN_WORK_PATH:=${TRAIN_CKPT_PATH}/work}"
mkdir -p "${TRAIN_CKPT_PATH}" "${TRAIN_LOG_PATH}" "${TRAIN_TF_EVENTS_PATH}" "${TRAIN_WORK_PATH}"
export TRAIN_LOG_PATH TRAIN_TF_EVENTS_PATH

echo "[run.sh] DATA=${TRAIN_DATA_PATH}"
echo "[run.sh] CKPT=${TRAIN_CKPT_PATH}"
echo "[run.sh] mode=multi-domain-moe (H010 anchor + H012 MMoE 4-expert routing)"

# ---------------------------------------------------------------------------
# Train (H010 envelope + H010 mechanism + H008 anchor + H012 NEW).
# Diff vs H010/upload/run.sh:
#   --use_multi_domain_moe          H012 NEW: Multi-domain MoE on NS-tokens 활성
#   --num_experts 4                 H012 NEW: 도메인 수 매핑
#   --moe_ffn_hidden 128            H012 NEW: 2 × d_model (sample-scale §10.6 budget)
# H010 mechanism flags (--use_ns_to_s_xattn --ns_xattn_num_heads 4 --log_attn_entropy) 그대로.
# H008 mechanism flags (--fusion_type dcn_v2 --dcn_v2_num_layers 2 --dcn_v2_rank 8) 그대로.
# ---------------------------------------------------------------------------
python3 -u "${SCRIPT_DIR}/train.py" \
    --num_epochs 10 \
    --patience 3 \
    --seed 42 \
    --ns_tokenizer_type rankmixer \
    --user_ns_tokens 5 \
    --item_ns_tokens 2 \
    --num_queries 2 \
    --ns_groups_json "" \
    --emb_skip_threshold 1000000 \
    --num_workers 2 \
    --buffer_batches 4 \
    --train_ratio 0.3 \
    --seq_max_lens "seq_a:64,seq_b:64,seq_c:128,seq_d:128" \
    --use_label_time_split \
    --oof_user_ratio 0.1 \
    --split_seed 42 \
    --fusion_type dcn_v2 \
    --dcn_v2_num_layers 2 \
    --dcn_v2_rank 8 \
    --use_ns_to_s_xattn \
    --ns_xattn_num_heads 4 \
    --log_attn_entropy \
    --use_multi_domain_moe \
    --num_experts 4 \
    --moe_ffn_hidden 128 \
    "$@"

echo "[run.sh] training complete (multi-domain-moe); metrics at ${TRAIN_CKPT_PATH}/metrics.json"
