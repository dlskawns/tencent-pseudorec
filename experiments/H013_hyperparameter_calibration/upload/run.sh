#!/bin/bash
# Platform run.sh for H013_hyperparameter_calibration.
# Measurement diagnostic H — H010 mechanism + envelope byte-identical.
# Goal: ceiling 의 정체 결정 (hyperparameter artifact vs mechanism limit).
#
# Mutation vs H010/upload/run.sh:
#   --batch_size 2048               H013 NEW: 사용자 override 명시 bake (default 256)
#   --lr 8e-4                       H013 NEW: linear scaling rule (batch 2048 = 8× × default lr)
#   --num_workers 4                 H013 NEW: 2 → 4 (Taiji 안전 범위, deadlock 위험 8 미만)
#   --buffer_batches 8              H013 NEW: 4 → 8 (큰 배치에서 IO bound 완화)
#
# H010 mechanism flags (--use_ns_to_s_xattn --ns_xattn_num_heads 4 --log_attn_entropy) 그대로.
# H008 mechanism flags (--fusion_type dcn_v2 --dcn_v2_num_layers 2 --dcn_v2_rank 8) 그대로.
# §17.2 parametric mutation justified by H012 F-2 (effective lr 1/8 underpowered 가설).
# Generated by H013 build flow — do not hand-edit on platform.
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
export PYTHONPATH="${SCRIPT_DIR}:${PYTHONPATH:-}"

# ---------------------------------------------------------------------------
# Platform must set TRAIN_DATA_PATH and TRAIN_CKPT_PATH. Other paths derive.
# ---------------------------------------------------------------------------
: "${TRAIN_DATA_PATH:?TRAIN_DATA_PATH not set by platform}"
: "${TRAIN_CKPT_PATH:?TRAIN_CKPT_PATH not set by platform}"
: "${TRAIN_LOG_PATH:=${TRAIN_CKPT_PATH}/logs}"
: "${TRAIN_TF_EVENTS_PATH:=${TRAIN_CKPT_PATH}/tf_events}"
: "${TRAIN_WORK_PATH:=${TRAIN_CKPT_PATH}/work}"
mkdir -p "${TRAIN_CKPT_PATH}" "${TRAIN_LOG_PATH}" "${TRAIN_TF_EVENTS_PATH}" "${TRAIN_WORK_PATH}"
export TRAIN_LOG_PATH TRAIN_TF_EVENTS_PATH

echo "[run.sh] DATA=${TRAIN_DATA_PATH}"
echo "[run.sh] CKPT=${TRAIN_CKPT_PATH}"
echo "[run.sh] mode=hyperparameter-calibration (H010 mechanism + lr 8e-4 + batch 2048 explicit + IO 4/8)"

# ---------------------------------------------------------------------------
# Train (H010 mechanism + envelope, calibrated hyperparameters).
# ---------------------------------------------------------------------------
python3 -u "${SCRIPT_DIR}/train.py" \
    --num_epochs 10 \
    --patience 3 \
    --seed 42 \
    --batch_size 2048 \
    --lr 8e-4 \
    --ns_tokenizer_type rankmixer \
    --user_ns_tokens 5 \
    --item_ns_tokens 2 \
    --num_queries 2 \
    --ns_groups_json "" \
    --emb_skip_threshold 1000000 \
    --num_workers 4 \
    --buffer_batches 8 \
    --train_ratio 0.3 \
    --seq_max_lens "seq_a:64,seq_b:64,seq_c:128,seq_d:128" \
    --use_label_time_split \
    --oof_user_ratio 0.1 \
    --split_seed 42 \
    --fusion_type dcn_v2 \
    --dcn_v2_num_layers 2 \
    --dcn_v2_rank 8 \
    --use_ns_to_s_xattn \
    --ns_xattn_num_heads 4 \
    --log_attn_entropy \
    "$@"

echo "[run.sh] training complete (hyperparameter-calibration); metrics at ${TRAIN_CKPT_PATH}/metrics.json"
