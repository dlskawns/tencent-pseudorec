#!/bin/bash
# Platform run.sh for H016_oof_future_redefine.
# OOF 재정의 (label_time future-only holdout). cohort drift 처리 — paradigm 안 마지막 시도.
# - H010 mechanism (NS xattn) + H008 mechanism (DCN-V2 fusion) + envelope byte-identical.
# - Single mutation: --oof_split_type random_user → future_label_time.
#   - random_user (H010 default): OOF = random 10% users (random user holdout).
#   - future_label_time (H016): OOF = rows with label_time >= quantile(label_time, 0.9).
#                               OOF distribution = pure future = closer to platform eval.
# - 코드 변경: dataset.py (_RowFilter + split function: oof_cutoff branch),
#   train.py (CLI flag --oof_split_type).
# - model.py / infer.py / trainer.py / utils.py byte-identical.
# - **Paired Δ baseline 깨짐 인정** — OOF 정의 자체 변경 → prior H 들 OOF AUC 비교 invalid.
#   단 Platform AUC 비교는 valid (eval data 동일).
# - Goal: 4-layer ceiling diagnosis 의 마지막 가설 L2 (cohort drift) 의 다른 form (Frame C).
#   H015 (recency loss weighting) 와 같이 보면 cohort drift 의 두 form 동시 검증.
# - §17.4 temporal_cohort 카테고리 (H015 first-touch 와 같음 — sub-form).
# - §18 inference 인프라 룰 inherit (infer.py 변경 없음).
# Generated by H016 build flow — do not hand-edit on platform.
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
export PYTHONPATH="${SCRIPT_DIR}:${PYTHONPATH:-}"

: "${TRAIN_DATA_PATH:?TRAIN_DATA_PATH not set by platform}"
: "${TRAIN_CKPT_PATH:?TRAIN_CKPT_PATH not set by platform}"
: "${TRAIN_LOG_PATH:=${TRAIN_CKPT_PATH}/logs}"
: "${TRAIN_TF_EVENTS_PATH:=${TRAIN_CKPT_PATH}/tf_events}"
: "${TRAIN_WORK_PATH:=${TRAIN_CKPT_PATH}/work}"
mkdir -p "${TRAIN_CKPT_PATH}" "${TRAIN_LOG_PATH}" "${TRAIN_TF_EVENTS_PATH}" "${TRAIN_WORK_PATH}"
export TRAIN_LOG_PATH TRAIN_TF_EVENTS_PATH

echo "[run.sh] DATA=${TRAIN_DATA_PATH}"
echo "[run.sh] CKPT=${TRAIN_CKPT_PATH}"
echo "[run.sh] mode=oof-future-redefine (H010 mechanism + OOF = label_time future-only holdout)"

# ---------------------------------------------------------------------------
# Diff vs H010/upload/run.sh:
#   --oof_split_type future_label_time   H016 NEW (was 'random_user' default)
#   --batch_size 2048                    H016 NEW (사용자 prior regime 명시 bake)
#   --lr 1e-4                            H016 NEW (H010 default 명시 bake)
# All other H010 flags 그대로.
# ---------------------------------------------------------------------------
python3 -u "${SCRIPT_DIR}/train.py" \
    --num_epochs 10 \
    --patience 3 \
    --seed 42 \
    --batch_size 2048 \
    --lr 1e-4 \
    --ns_tokenizer_type rankmixer \
    --user_ns_tokens 5 \
    --item_ns_tokens 2 \
    --num_queries 2 \
    --ns_groups_json "" \
    --emb_skip_threshold 1000000 \
    --num_workers 2 \
    --buffer_batches 4 \
    --train_ratio 0.3 \
    --seq_max_lens "seq_a:64,seq_b:64,seq_c:128,seq_d:128" \
    --use_label_time_split \
    --oof_user_ratio 0.1 \
    --split_seed 42 \
    --oof_split_type future_label_time \
    --fusion_type dcn_v2 \
    --dcn_v2_num_layers 2 \
    --dcn_v2_rank 8 \
    --use_ns_to_s_xattn \
    --ns_xattn_num_heads 4 \
    --log_attn_entropy \
    "$@"

echo "[run.sh] training complete (oof-future-redefine); metrics at ${TRAIN_CKPT_PATH}/metrics.json"
